def kdtree_index(X, seg):
    nb = X.shape[0]
    for i in range(nb):
        x = X[i, ...]
        seg_i = seg[i, ...]
        T = cKDTree(x)
        X[i, ...] = np.take(x, T.indices, axis=0)
        seg[i, ...] = np.take(seg_i, T.indices, axis=0)


def kd_mean_pool(x, seg, pool_size):
    x = np.swapaxes(x, 1, -1)
    r = len(list(x.shape))
    if r == 4:
        x = np.reshape(x, (x.shape[0], x.shape[1], x.shape[2], -1, pool_size))
    if r == 3:
        x = np.reshape(x, (x.shape[0], x.shape[1], -1, pool_size))
    seg = np.reshape(seg, (seg.shape[0], -1, pool_size))
    out = np.swapaxes(np.mean(x, axis=-1, keepdims=False), 1, -1)
    seg = np.squeeze(stats.mode(seg, axis=2)[0], axis=2)
    return out, seg


